def canonicalize_subject(subject: str) -> str:
    """إرجاع رمز قانوني للمادة (math/arabic/french/…) أو ``generic``."""
    if not subject:
        return "generic"
    key = subject.strip()
    return _SUBJECT_ALIASES.get(key, "generic")


