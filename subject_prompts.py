def get_subject_guidance(subject: str) -> str:
    """
    إرجاع كتلة توجيهات خاصّة بالمادة لتضاف إلى system_prompt.

    Args:
        subject: اسم المادة كما يدخله المستخدم (عربي، فرنسي، إنجليزي أو canonical).

    Returns:
        نصّ يبدأ بفاصل سطر ويحوي البنية الرسمية والكفاءات وممنوعات المادة.
        إذا لم تُعرَف المادة يُرجَع سلسلة فارغة (سلوك متحفّظ).
    """
    key = canonicalize_subject(subject)
    return _BY_SUBJECT.get(key, "")


def is_language_subject(subject: str) -> bool:
    """هل المادة لغوية (تستعمل نصّاً + قواعد + إنتاج كتابي)؟"""
    return canonicalize_subject(subject) in {"arabic", "french", "english"}


def is_non_math_subject(subject: str) -> bool:
    """هل المادة غير حسابية (يُمنع فيها LaTeX الرياضي والأسئلة الحسابية)؟"""
    return canonicalize_subject(subject) in {
        "arabic",
        "french",
        "english",
        "history",
        "islamic",
        "civic",
        "philosophy",
    }
